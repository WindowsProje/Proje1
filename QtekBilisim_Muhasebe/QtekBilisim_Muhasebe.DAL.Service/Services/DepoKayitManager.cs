        public async Task<bool> DepoKayitEkleAsAsync(DepoKayitTumDTO depo)
        {
            try
            {
                using (var unitOfWork = new UnitOfWork(new QtekBilisim_MuhasebeContext()))
                {
                    unitOfWork.DepoKayitlari.AddData(new DepoKayit()
                    {
                        AktifMi = depo.AktifMi,
                        DepoAd = depo.DepoAd,
                        DepoKod = depo.DepoKod,
                        DilID = depo.DilID,
                        GuncellemeTarih = depo.GuncellemeTarih,
                        KayitTarih = depo.KayitTarih,
                        SilindiMi = depo.SilindiMi,
                        SirketID = depo.SirketID,
                        StokID = depo.StokID,
                        Yetkili = depo.Yetkili
                    });
                    int affect = await unitOfWork.CompleteAsync();
                    if (affect > 0)
                    {
                        return true;
                    }
                    else
                    {
                        return false;
                    }
                }
            }
            catch (DbEntityValidationException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new DbEntityValidationException(code.Result);
            }
            catch (ArgumentNullException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new ArgumentNullException(code.Result);
            }
            catch (NullReferenceException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new NullReferenceException(code.Result);
            }
            catch (AggregateException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new AggregateException(code.Result);
            }
            catch (FormatException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new FormatException(code.Result);
            }
            catch (Exception error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new Exception(code.Result);
            }
        }
        public async Task<bool> DepoKayitGuncelleAsAsync(DepoKayitTumDTO depo)
        {
            try
            {
                using (var unitOfWork = new UnitOfWork(new QtekBilisim_MuhasebeContext()))
                {
                    var temp = unitOfWork.DepoKayitlari.FindDataAsync(depo.DepoID);
                    if (temp.Result != null)
                    {
                        temp.Result.AktifMi = depo.AktifMi;
                        temp.Result.DepoAd = depo.DepoAd;
                        temp.Result.DepoKod = depo.DepoKod;
                        temp.Result.DilID = depo.DilID;
                        temp.Result.GuncellemeTarih = depo.GuncellemeTarih;
                        temp.Result.SilindiMi = depo.SilindiMi;
                        temp.Result.StokID = depo.StokID;
                        temp.Result.Yetkili = depo.Yetkili;
                        if (unitOfWork.DepoKayitlari.IsModified(temp.Result) == true)
                        {
                            int affect = await unitOfWork.CompleteAsync();
                            if (affect > 0)
                            {
                                return true;
                            }
                            else
                            {
                                return false;
                            }
                        }
                        else
                        {
                            throw new NotModifiedDataOnDatabaseCustomException();
                        }
                    }
                    else
                    {
                        throw new NullDataOnDatabaseCustomException();
                    }
                }
            }
            catch (DbEntityValidationException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new DbEntityValidationException(code.Result);
            }
            catch (ArgumentNullException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new ArgumentNullException(code.Result);
            }
            catch (NullReferenceException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new NullReferenceException(code.Result);
            }
            catch (AggregateException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new AggregateException(code.Result);
            }
            catch (FormatException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new FormatException(code.Result);
            }
            catch (NullDataOnDatabaseCustomException)
            {
                throw new NullDataOnDatabaseCustomException();
            }
            catch (NotModifiedDataOnDatabaseCustomException)
            {
                throw new NotModifiedDataOnDatabaseCustomException();
            }
            catch (Exception error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new Exception(code.Result);
            }
        }
