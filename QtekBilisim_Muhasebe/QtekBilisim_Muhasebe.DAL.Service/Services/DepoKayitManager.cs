        public DepoKayitListeDTO[] DepoKayitListeAsAsync(DepoKayitFiltreDTO depo)
        {
            try
            {
                using (var unitOfWork = new UnitOfWork(new QtekBilisim_MuhasebeContext()))
                {
                    List<DepoKayitListeDTO> lst = new List<DepoKayitListeDTO>();
                    foreach (var item in unitOfWork.DepoKayitlari.ListDataByExpressionAsync(b => b.AktifMi == depo.AktifMi && b.DilID == depo.DilID && b.SirketID == depo.SirketID && b.SilindiMi == depo.SilindiMi).Result)
                    {
                        lst.Add(new DepoKayitListeDTO()
                        {
                            DepoAd = item.DepoAd,
                            DepoID = item.DepoID,
                            DepoKod = item.DepoKod,
