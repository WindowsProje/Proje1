        }
        public KdvOranListeDTO[] KdvOranListeleAsAsync(KdvOranFiltreDTO kdv)
        {
            try
            {
                using (var unitOfWork = new UnitOfWork(new QtekBilisim_MuhasebeContext()))
                {
                    List<KdvOranListeDTO> lst = new List<KdvOranListeDTO>();
                    foreach (var item in unitOfWork.KdvOranlari.ListDataByExpressionAsync(c => c.AktifMi == kdv.AktifMi && c.DilID == kdv.DilID && c.SirketID == kdv.SirketID && c.SilindiMi == kdv.SilindiMi).Result)
                    {
                        lst.Add(new KdvOranListeDTO()
                        {
                            KDV = item.KDV,
                            KdvID = item.KDV
                        });
                    }
                    return lst.ToArray();
                }
            }
            catch (ArgumentNullException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new ArgumentNullException(code.Result);
            }
            catch (NullReferenceException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new NullReferenceException(code.Result);
            }
            catch (AggregateException error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new AggregateException(code.Result);
            }
            catch (Exception error)
            {
                var code = HataKayitManager.HataKayitEkleAsync(error);
                throw new Exception(code.Result);
            }
        }
    }
